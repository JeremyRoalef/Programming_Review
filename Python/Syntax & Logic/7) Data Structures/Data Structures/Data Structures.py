#Common Data Structures:

#Lists - Changeable values and length of structure can increase and decrease
my_list = [1,2,3]

#Tuples - values and size cannot change after creation
my_tuple = (1,2,3)

#Sets - Each value is unique in the data structure (value1 cannot equal value 2, 3, and so on)
my_set = {1,2,3}

#Dictionaries - Like lists, but uses keys to find values instead of an index
my_dictionary = {1: "value1", 2: "value2", 3: "value3"}
